import sys

sys.setrecursionlimit(10 ** 9)

sys.stdin = open("2410-2의 멱수의 합-3.txt", "r")

input = sys.stdin.readline

N = int(input())

dp = list(range(max(3,N+1)))

for i in range(2, N+1):
    if i % 2 == 1:
        dp[i] = dp[i-1]
    else:
        dp[i] = dp[i-1] + dp[i//2]

print(dp[N] % 1000000000)


